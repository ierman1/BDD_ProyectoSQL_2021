    private void saveCache() {

        //cojo el personaje CACHE
        Personaje p = Personaje.newPesonaje;

        if (selectedClase != null) {
            p.setClase(selectedClase);
        }
    }

    private void loadCache() {

        //cojo el personaje CACHE
        Personaje p = Personaje.newPesonaje;

        Clase c = p.getClase();
        if (c == null) return;
        if (c.getId() == 0) return;

        comboBoxClass.setSelectedItem(c.getNombre());
    }
